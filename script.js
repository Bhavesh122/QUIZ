let currentQuestionIndex = 0;
let score = 0;
let correctAnswers = 0;
let wrongAnswers = 0;
let wrongQuestions = [];

function loadQuestion() {
    const quizDiv = document.getElementById('quiz');
    quizDiv.innerHTML = `
        <h3>${questions[currentQuestionIndex].question}</h3>
        ${questions[currentQuestionIndex].options.map((option, index) => `
            <label>
                <input type="radio" name="answer" value="${index}">
                ${option}
            </label><br>
        `).join('')}
    `;
}

function nextQuestion() {
    const selectedOption = document.querySelector('input[name="answer"]:checked');
    if (selectedOption) {
        const answer = parseInt(selectedOption.value);
        if (answer === questions[currentQuestionIndex].correct) {
            score += 1;
            correctAnswers += 1;
        } else {
            wrongAnswers += 1;
            wrongQuestions.push({
                question: questions[currentQuestionIndex].question,
                selectedAnswer: questions[currentQuestionIndex].options[answer],
                correctAnswer: questions[currentQuestionIndex].options[questions[currentQuestionIndex].correct]
            });
        }

        currentQuestionIndex += 1;

        if (currentQuestionIndex < questions.length) {
            loadQuestion();
        } else {
            document.getElementById('next').style.display = 'none';
            document.getElementById('submit').style.display = 'block';
        }
    } else {
        alert("Please select an answer before proceeding.");
    }
}

function submitQuiz() {
    // Hide the quiz container and show the result container
    document.getElementById('quiz-container').style.display = 'none';
    document.getElementById('result-container').style.display = 'block';

    // Update the score and the result counters
    document.getElementById('score').innerText = score;
    document.getElementById('correct').innerText = correctAnswers;
    document.getElementById('wrong').innerText = wrongAnswers;

    // Clear previous content in wrong questions div
    const wrongDiv = document.getElementById('wrong-questions');
    wrongDiv.innerHTML = ''; // Clear any old data

    // If there are wrong answers, display them
    if (wrongQuestions.length > 0) {
        let wrongQuestionsHTML = '<h3>Incorrect Questions:</h3>';
        wrongQuestions.forEach((item, index) => {
            wrongQuestionsHTML += `
                <div>
                    <p><b>Question ${index + 1}:</b> ${item.question}</p>
                    <p><b>Your Answer:</b> ${item.selectedAnswer}</p>
                    <p><b>Correct Answer:</b> ${item.correctAnswer}</p>
                    <hr>
                </div>
            `;
        });
        wrongDiv.innerHTML = wrongQuestionsHTML;
    } else {
        // If all answers are correct
        wrongDiv.innerHTML = "<p>All answers are correct!</p>";
    }

    // Scroll to the top of the result container when showing results
    window.scrollTo(0, 0); // Ensure the window scrolls to the top too
}




function restartQuiz() {
    score = 0;
    correctAnswers = 0;
    wrongAnswers = 0;
    wrongQuestions = [];
    currentQuestionIndex = 0;
    document.getElementById('result-container').style.display = 'none';
    document.getElementById('quiz-container').style.display = 'block';
    document.getElementById('next').style.display = 'block';
    document.getElementById('submit').style.display = 'none';
    loadQuestion();
}
// Show the quiz and hide the intro
function startQuiz() {
    document.getElementById('intro-container').style.display = 'none';
    document.getElementById('quiz-container').style.display = 'block';
    loadQuestion(); // Load the first question
}


window.onload = loadQuestion;